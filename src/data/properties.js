export function getPropertyBySlug(slug) {
  return properties.find((property) => property.slug === slug) ?? null;
}

export function getPropertiesByType(type) {
  return properties.filter((property) => property.type === type);
}

export function getPropertiesByAgent(agentSlug) {
  return properties.filter((property) => property.agentSlug === agentSlug);
}

export function getRelatedProperties(slug, limit = 3) {
  const property = getPropertyBySlug(slug);
  if (!property) return [];
  return properties
    .filter((item) => item.slug !== slug && item.type === property.type)
    .slice(0, limit);
}

